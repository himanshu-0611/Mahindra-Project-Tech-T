const mongoose = require("mongoose");
const bcrypt = require("bcryptjs");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  work: {
    type: String,
    require: true,
  },
  phone: {
    type: Number,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
  cpassword: {
    type: String,
    require: true,
  },
});

// we are hashing the password

userSchema.pre("save", async function (next) {
  // when we need this keyword, it works totally opposite when we use arrow function, this function returns promises, so we make it async
  console.log("inside")
  if (this.isModified("password")) {
    this.password = await bcrypt.hash(this.password, 12);
    this.cpassword = await bcrypt.hash(this.cpassword, 12);
  }
});

const User = mongoose.model("USER", userSchema); //User is what is in collection - users

module.exports = User;
